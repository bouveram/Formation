package com.m2i.formation.media;

public class Item {

	private double price;
	private int id;
	private String name;
	
	public double getPrice() {
		return price;
	}
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
